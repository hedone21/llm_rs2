def load_csv(path):
    """Load score_distribution.csv → list of (position, score, token_type)."""
    rows = []
    with open(path) as f:
        reader = csv.DictReader(f)
        for row in reader:
            rows.append({
                "position": int(row["position"]),
                "score": float(row["score"]),
                "token_type": row["token_type"],
            })
    return rows


def basic_stats(values):
    """Compute mean, std, min, max, median, cv, skewness."""
    n = len(values)
    if n == 0:
        return {}
    s = sorted(values)
    mean = sum(values) / n
    var = sum((x - mean) ** 2 for x in values) / n
    std = math.sqrt(var)
    median = s[n // 2] if n % 2 else (s[n // 2 - 1] + s[n // 2]) / 2
    cv = std / mean if mean != 0 else float("inf")
    skewness = (
        sum((x - mean) ** 3 for x in values) / (n * std ** 3) if std > 0 else 0
    )
    return {
        "n": n, "mean": mean, "std": std, "min": s[0], "max": s[-1],
        "median": median, "cv": cv, "skewness": skewness,
        "p25": s[n // 4], "p75": s[3 * n // 4],
    }


def shannon_entropy(values):
    """Normalized Shannon entropy (0=concentrated, 1=uniform)."""
    total = sum(values)
    if total <= 0:
        return 0.0, 0.0, 0.0
    n = len(values)
    h = -sum((v / total) * math.log(v / total) for v in values if v > 0)
    h_max = math.log(n) if n > 1 else 1.0
    return h, h_max, h / h_max


def gini_coefficient(values):
    """Gini coefficient (0=equality, 1=inequality)."""
    s = sorted(values)
    n = len(s)
    total = sum(s)
    if total == 0 or n == 0:
        return 0.0
    weighted_sum = sum((2 * (i + 1) - n - 1) * s[i] for i in range(n))
    return weighted_sum / (n * total)


def pearson_r(xs, ys):
    """Pearson correlation coefficient."""
    n = len(xs)
    mx = sum(xs) / n
    my = sum(ys) / n
    cov = sum((xs[i] - mx) * (ys[i] - my) for i in range(n))
    vx = sum((x - mx) ** 2 for x in xs)
    vy = sum((y - my) ** 2 for y in ys)
    denom = math.sqrt(vx * vy)
    return cov / denom if denom > 0 else 0.0


def spearman_rho(xs, ys):
    """Spearman rank correlation."""
    def rankdata(v):
        indexed = sorted(range(len(v)), key=lambda i: v[i])
        ranks = [0.0] * len(v)
        for rank, idx in enumerate(indexed):
            ranks[idx] = float(rank)
        return ranks

    return pearson_r(rankdata(xs), rankdata(ys))


def text_histogram(values, bins=15, width=40, label=""):
    """ASCII histogram."""
    if not values:
        return ""
    lo, hi = min(values), max(values)
    if lo == hi:
        return f"  All values = {lo:.4f}\n"

    bin_width = (hi - lo) / bins
    counts = [0] * bins
    for v in values:
        idx = min(int((v - lo) / bin_width), bins - 1)
        counts[idx] += 1

    max_count = max(counts)
    lines = []
    if label:
        lines.append(f"  {label}")
    for i in range(bins):
        lo_edge = lo + i * bin_width
        hi_edge = lo_edge + bin_width
        bar_len = int(counts[i] / max_count * width) if max_count > 0 else 0
        bar = "█" * bar_len
        pct = 100.0 * counts[i] / len(values)
        lines.append(f"  [{lo_edge:8.2f} - {hi_edge:8.2f}) {bar:<{width}} {counts[i]:>4} ({pct:5.1f}%)")
    return "\n".join(lines)


def simulate_h2o(rows, protected_prefix=4, target=512, keep_ratio=0.5):
    """Simulate H2O token selection. Returns (hh, evicted, recent) token lists."""
    available = target - protected_prefix
    hh_budget = int(available * keep_ratio)
    recent_budget = available - hh_budget
    cache_len = len(rows)
    recent_start = cache_len - recent_budget

    evictable = [(r["position"], r["score"], r["token_type"])
                 for r in rows if protected_prefix <= r["position"] < recent_start]
    evictable.sort(key=lambda x: x[1], reverse=True)

    hh = evictable[:hh_budget]
    evicted = evictable[hh_budget:]
    recent = [(r["position"], r["score"], r["token_type"])
              for r in rows if r["position"] >= recent_start]

    return {
        "hh": hh, "evicted": evicted, "recent": recent,
        "hh_budget": hh_budget, "recent_budget": recent_budget,
        "protected_prefix": protected_prefix, "target": target,
    }


def generate_report(rows):
    """Generate full markdown analysis report."""
    # Categorize tokens
    by_type = defaultdict(list)
    for r in rows:
        by_type[r["token_type"]].append(r["score"])

    all_scores = [r["score"] for r in rows]
    non_bos = [r["score"] for r in rows if r["token_type"] != "bos"]
    gen_only = [(r["position"], r["score"]) for r in rows if r["token_type"] == "generated"]

    bos_score = by_type["bos"][0] if by_type["bos"] else 0.0

    # Stats
    overall = basic_stats(all_scores)
    non_bos_stats = basic_stats(non_bos)
    prompt_stats = basic_stats(by_type.get("prompt", []))
    struct_stats = basic_stats(by_type.get("structural", []))
    gen_stats = basic_stats(by_type.get("generated", []))

    # Entropy & Gini (non-BOS)
    h, h_max, h_norm = shannon_entropy(non_bos)
    gini = gini_coefficient(non_bos)

    # Sigma analysis (non-BOS)
    above_1s = sum(1 for s in non_bos if s > non_bos_stats["mean"] + non_bos_stats["std"])
    above_2s = sum(1 for s in non_bos if s > non_bos_stats["mean"] + 2 * non_bos_stats["std"])

    # Position-score correlation (generated only)
    gen_pos = [p for p, _ in gen_only]
    gen_val = [s for _, s in gen_only]
    pr = pearson_r(gen_pos, gen_val)
    sr = spearman_rho(gen_pos, gen_val)

    # H2O simulation
    h2o = simulate_h2o(rows)
    hh_scores = [s for _, s, _ in h2o["hh"]]
    ev_scores = [s for _, s, _ in h2o["evicted"]]
    hh_stats = basic_stats(hh_scores)
    ev_stats = basic_stats(ev_scores)
    hh_prompt = sum(1 for _, _, t in h2o["hh"] if t in ("prompt", "structural"))
    hh_gen = sum(1 for _, _, t in h2o["hh"] if t == "generated")

    # Random baseline (every 2nd evictable token)
    evictable_all = [(r["position"], r["score"]) for r in rows
                     if h2o["protected_prefix"] <= r["position"] < len(rows) - h2o["recent_budget"]]
    random_sel = evictable_all[::2][:h2o["hh_budget"]]
    random_avg = sum(s for _, s in random_sel) / len(random_sel) if random_sel else 0

    # HH score margin: how much better is the worst HH than the best evicted?
    hh_min_score = min(hh_scores) if hh_scores else 0
    ev_max_score = max(ev_scores) if ev_scores else 0
    margin = hh_min_score - ev_max_score

    # Build report
    lines = []
    lines.append("# HH Meaninglessness Proof: Score Distribution Analysis")
    lines.append("")
    lines.append("## Executive Summary")
    lines.append("")
    lines.append("This report analyzes simulated attention score distributions for Llama 3.2 1B")
    lines.append("to determine whether H2O's Heavy Hitter (HH) selection provides meaningful")
    lines.append("differentiation beyond simple sliding window eviction.")
    lines.append("")
    lines.append(f"**Verdict**: {'HH selection is MEANINGLESS' if h_norm > 0.85 and margin < gen_stats['std'] else 'HH selection shows some differentiation'}")
    lines.append("")

    # Simulation parameters
    lines.append("## 1. Simulation Parameters")
    lines.append("")
    lines.append("| Parameter | Value |")
    lines.append("|-----------|-------|")
    lines.append(f"| Model | Llama 3.2 1B (simulated) |")
    lines.append(f"| Q-heads / KV-heads | 32 / 8 (GQA) |")
    lines.append(f"| Layers | 16 |")
    lines.append(f"| Prompt length | 128 tokens |")
    lines.append(f"| Cache at eviction | {len(rows)} tokens |")
    lines.append(f"| Decode steps | {len(rows) - 128} |")
    lines.append(f"| Score aggregation | Within-step: MAX across layers, Across-steps: SUM |")
    lines.append(f"| Calibration target | Round 15 observations (BOS≈3003, prompt≈3.3, gen≈33) |")
    lines.append("")

    # Distribution analysis
    lines.append("## 2. Score Distribution (Experiment 1)")
    lines.append("")
    lines.append("### 2.1 Per-Category Statistics")
    lines.append("")
    lines.append("| Category | N | Mean | Std | Min | Max | Median | CV |")
    lines.append("|----------|---|------|-----|-----|-----|--------|-----|")
    lines.append(f"| BOS | 1 | {bos_score:.1f} | — | — | — | — | — |")
    if struct_stats:
        lines.append(f"| Structural | {struct_stats['n']} | {struct_stats['mean']:.3f} | {struct_stats['std']:.3f} | {struct_stats['min']:.3f} | {struct_stats['max']:.3f} | {struct_stats['median']:.3f} | {struct_stats['cv']:.3f} |")
    if prompt_stats:
        lines.append(f"| Prompt | {prompt_stats['n']} | {prompt_stats['mean']:.3f} | {prompt_stats['std']:.3f} | {prompt_stats['min']:.3f} | {prompt_stats['max']:.3f} | {prompt_stats['median']:.3f} | {prompt_stats['cv']:.3f} |")
    if gen_stats:
        lines.append(f"| Generated | {gen_stats['n']} | {gen_stats['mean']:.3f} | {gen_stats['std']:.3f} | {gen_stats['min']:.3f} | {gen_stats['max']:.3f} | {gen_stats['median']:.3f} | {gen_stats['cv']:.3f} |")
    lines.append("")

    lines.append("### 2.2 Scale Ratios")
    lines.append("")
    lines.append(f"- **BOS / Prompt**: {bos_score / prompt_stats['mean']:.0f}x")
    lines.append(f"- **BOS / Generated**: {bos_score / gen_stats['mean']:.0f}x")
    if struct_stats:
        lines.append(f"- **Structural / Prompt**: {struct_stats['mean'] / prompt_stats['mean']:.1f}x")
    lines.append(f"- **Generated / Prompt**: {gen_stats['mean'] / prompt_stats['mean']:.1f}x")
    lines.append("")

    # Histogram
    lines.append("### 2.3 Score Distribution Histogram (excluding BOS)")
    lines.append("")
    lines.append("```")
    lines.append(text_histogram(non_bos, bins=20, width=35))
    lines.append("```")
    lines.append("")

    # Generated tokens only histogram
    lines.append("### 2.4 Generated Tokens Score Distribution")
    lines.append("")
    lines.append("```")
    lines.append(text_histogram(gen_val, bins=15, width=35, label="Generated tokens only"))
    lines.append("```")
    lines.append("")

    # Information theory
    lines.append("### 2.5 Information-Theoretic Analysis (excluding BOS)")
    lines.append("")
    lines.append("| Metric | Value | Interpretation |")
    lines.append("|--------|-------|----------------|")
    lines.append(f"| Shannon Entropy (H) | {h:.4f} | |")
    lines.append(f"| Max Entropy (H_max) | {h_max:.4f} | (= ln N, uniform dist.) |")
    lines.append(f"| Normalized Entropy | **{h_norm:.4f}** | {'≈ uniform → no HH differentiation' if h_norm > 0.85 else 'some structure exists'} |")
    lines.append(f"| Gini Coefficient | **{gini:.4f}** | {'low inequality → scores undifferentiated' if gini < 0.3 else 'moderate inequality'} |")
    lines.append(f"| CV (σ/μ) | {non_bos_stats['cv']:.4f} | {'low variation' if non_bos_stats['cv'] < 0.5 else 'moderate variation'} |")
    lines.append(f"| Skewness | {non_bos_stats['skewness']:.4f} | {'right-skewed (recency)' if non_bos_stats['skewness'] > 0.5 else 'roughly symmetric'} |")
    lines.append("")

    lines.append("### 2.6 Sigma Outlier Analysis (excluding BOS)")
    lines.append("")
    lines.append(f"- Tokens > mean + 1σ: **{above_1s}** / {len(non_bos)} ({100*above_1s/len(non_bos):.1f}%)")
    lines.append(f"- Tokens > mean + 2σ: **{above_2s}** / {len(non_bos)} ({100*above_2s/len(non_bos):.1f}%)")
    lines.append("")
    if above_2s > 0:
        top_outliers = sorted([(r["position"], r["score"], r["token_type"]) for r in rows
                               if r["score"] > non_bos_stats["mean"] + 2 * non_bos_stats["std"]
                               and r["token_type"] != "bos"],
                              key=lambda x: x[1], reverse=True)[:10]
        lines.append("Top >2σ tokens:")
        lines.append("")
        lines.append("| Position | Score | Type | Distance from end |")
        lines.append("|----------|-------|------|-------------------|")
        for pos, score, ttype in top_outliers:
            dist = len(rows) - 1 - pos
            lines.append(f"| {pos} | {score:.3f} | {ttype} | {dist} |")
        lines.append("")

    # Position-score correlation (Experiment 3)
    lines.append("## 3. Position-Score Correlation (Experiment 3)")
    lines.append("")
    lines.append("### 3.1 Generated Tokens: Position vs Score")
    lines.append("")
    lines.append("| Metric | Value | Interpretation |")
    lines.append("|--------|-------|----------------|")
    def corr_label(r):
        ar = abs(r)
        sign = "positive" if r > 0 else "negative"
        if ar > 0.7: return f"STRONG {sign}"
        elif ar > 0.4: return f"MODERATE {sign}"
        else: return "WEAK"
    lines.append(f"| Pearson r | **{pr:.4f}** | {corr_label(pr)} |")
    lines.append(f"| Spearman ρ | **{sr:.4f}** | {corr_label(sr)} |")
    lines.append("")

    if abs(pr) > 0.7 and pr < 0:
        lines.append("> **Strong negative correlation**: scores are dominated by **time-in-cache**.")
        lines.append("> Older generated tokens accumulate more score simply by existing longer,")
        lines.append("> not because they receive more attention per step.")
        lines.append("> H2O selects the **oldest** generated tokens as \"heavy hitters\" —")
        lines.append("> the exact **opposite** of sliding window, which keeps the most recent.")
        lines.append("> This \"reverse-recency\" bias is a fundamental flaw in cumulative")
        lines.append("> score accumulation without adequate decay.")
    elif abs(pr) > 0.7 and pr > 0:
        lines.append("> **Strong positive correlation**: scores are dominated by recency.")
        lines.append("> H2O's HH selection effectively picks the most recent non-window tokens,")
        lines.append("> making it a **strictly worse version of sliding window** (same tokens minus contiguity).")
    elif abs(pr) > 0.4:
        lines.append("> **Moderate correlation**: recency partially explains scores.")
        lines.append("> Some non-recent tokens may have genuinely high scores.")
    else:
        lines.append("> **Weak correlation**: scores are not dominated by recency.")
        lines.append("> HH selection captures genuinely different tokens from sliding window.")
    lines.append("")

    # Show quintile averages
    lines.append("### 3.2 Position Quintile Analysis (Generated tokens)")
    lines.append("")
    q_size = len(gen_only) // 5
    lines.append("| Quintile | Position Range | Avg Score | Interpretation |")
    lines.append("|----------|---------------|-----------|----------------|")
    for q in range(5):
        start = q * q_size
        end = start + q_size if q < 4 else len(gen_only)
        q_tokens = gen_only[start:end]
        q_avg = sum(s for _, s in q_tokens) / len(q_tokens)
        pos_lo = q_tokens[0][0]
        pos_hi = q_tokens[-1][0]
        label = ["Oldest", "Old", "Middle", "Recent", "Most recent"][q]
        lines.append(f"| Q{q+1} ({label}) | {pos_lo}-{pos_hi} | {q_avg:.3f} | |")
    lines.append("")

    # H2O simulation
    lines.append("## 4. H2O Simulation")
    lines.append("")
    lines.append(f"Settings: `protected_prefix={h2o['protected_prefix']}`, "
                 f"`target={h2o['target']}`, `keep_ratio=0.5`")
    lines.append("")
    lines.append("### 4.1 Budget Allocation")
    lines.append("")
    lines.append(f"- Protected prefix: {h2o['protected_prefix']} tokens (pos 0-{h2o['protected_prefix']-1})")
    lines.append(f"- HH budget: {h2o['hh_budget']} tokens")
    lines.append(f"- Recent window: {h2o['recent_budget']} tokens")
    lines.append(f"- Evicted: {len(h2o['evicted'])} tokens")
    lines.append("")

    lines.append("### 4.2 HH Selection Quality")
    lines.append("")
    lines.append("| Metric | HH Selected | Evicted | Random Baseline |")
    lines.append("|--------|-------------|---------|-----------------|")
    lines.append(f"| Count | {len(hh_scores)} | {len(ev_scores)} | {len(random_sel)} |")
    lines.append(f"| Avg Score | {hh_stats['mean']:.3f} | {ev_stats['mean']:.3f} | {random_avg:.3f} |")
    lines.append(f"| Std | {hh_stats['std']:.3f} | {ev_stats['std']:.3f} | — |")
    lines.append(f"| Min | {hh_stats['min']:.3f} | {ev_stats['min']:.3f} | — |")
    lines.append(f"| Max | {hh_stats['max']:.3f} | {ev_stats['max']:.3f} | — |")
    lines.append("")

    lines.append("### 4.3 Selection Ratios")
    lines.append("")
    hh_ev_ratio = hh_stats["mean"] / ev_stats["mean"] if ev_stats["mean"] > 0 else float("inf")
    hh_rand_ratio = hh_stats["mean"] / random_avg if random_avg > 0 else float("inf")
    lines.append(f"- **HH / Evicted**: {hh_ev_ratio:.2f}x")
    lines.append(f"- **HH / Random**: {hh_rand_ratio:.2f}x")
    lines.append(f"- **Score margin** (worst HH - best evicted): {margin:.3f}")
    lines.append(f"  - Margin / Generated σ: {margin / gen_stats['std']:.2f}σ")
    lines.append("")

    if hh_ev_ratio < 2.0:
        lines.append("> **HH/Evicted ratio < 2x**: The difference between \"important\" and")
        lines.append("> \"unimportant\" tokens is minimal. H2O's selection provides negligible benefit.")
    lines.append("")

    lines.append("### 4.4 HH Composition")
    lines.append("")
    lines.append(f"- From prompt: **{hh_prompt}** ({100*hh_prompt/len(hh_scores):.1f}%)")
    lines.append(f"- From generated: **{hh_gen}** ({100*hh_gen/len(hh_scores):.1f}%)")
    lines.append("")

    if hh_prompt > hh_gen:
        lines.append("> HH selection preferentially picks prompt tokens (low-score but selected")
        lines.append("> due to position in evictable range). These scattered prompt tokens")
        lines.append("> lack contextual coherence, degrading output quality.")
    else:
        lines.append("> HH selection picks mostly generated tokens, effectively replicating")
        lines.append("> sliding window but with gaps, losing contiguity.")
    lines.append("")

    # Top/Bottom HH
    lines.append("### 4.5 Top-10 and Bottom-10 HH Selected Tokens")
    lines.append("")
    lines.append("**Top-10 HH (highest scores):**")
    lines.append("")
    lines.append("| Rank | Position | Score | Type |")
    lines.append("|------|----------|-------|------|")
    for i, (pos, score, ttype) in enumerate(h2o["hh"][:10]):
        lines.append(f"| {i+1} | {pos} | {score:.3f} | {ttype} |")
    lines.append("")

    lines.append("**Bottom-10 HH (lowest scores in HH set):**")
    lines.append("")
    lines.append("| Rank | Position | Score | Type |")
    lines.append("|------|----------|-------|------|")
    for i, (pos, score, ttype) in enumerate(h2o["hh"][-10:]):
        lines.append(f"| {len(hh_scores)-9+i} | {pos} | {score:.3f} | {ttype} |")
    lines.append("")

    lines.append("**Top-5 Evicted (highest scores among evicted):**")
    lines.append("")
    lines.append("| Position | Score | Type | Note |")
    lines.append("|----------|-------|------|------|")
    for pos, score, ttype in h2o["evicted"][:5]:
        lines.append(f"| {pos} | {score:.3f} | {ttype} | Would have been HH rank {h2o['hh_budget']+1}+ |")
    lines.append("")

    # Conclusions
    lines.append("## 5. Conclusions")
    lines.append("")

    conclusions = []
    if h_norm > 0.85:
        conclusions.append(f"1. **Normalized entropy = {h_norm:.4f} ≈ 1.0**: Non-BOS score distribution is "
                          "near-uniform. There is insufficient variation to meaningfully distinguish "
                          "\"heavy hitters\" from ordinary tokens.")

    if gini < 0.3:
        conclusions.append(f"2. **Gini = {gini:.4f} (low inequality)**: Importance scores are "
                          "not concentrated in a few tokens — they are spread across all positions. "
                          "H2O's assumption of a bimodal distribution (BOS + scattered HH) fails.")

    if abs(pr) > 0.6:
        if pr < 0:
            conclusions.append(f"3. **Position-score Pearson r = {pr:.4f} (strong negative)**: "
                              "Cumulative scores are dominated by time-in-cache, not attention importance. "
                              "H2O selects the **oldest** generated tokens as HH — the exact opposite of "
                              "sliding window's recency. These old, scattered tokens lack contextual "
                              "relevance, explaining H2O's 3x worse performance vs sliding window.")
        else:
            conclusions.append(f"3. **Position-score Pearson r = {pr:.4f}**: Generated token scores are "
                              "strongly correlated with recency. H2O's score-based selection converges to "
                              "a **non-contiguous approximation of sliding window**, which is strictly worse "
                              "because it sacrifices contextual coherence.")

    if hh_ev_ratio < 2.0:
        conclusions.append(f"4. **HH/Evicted ratio = {hh_ev_ratio:.2f}x**: The average score of HH-selected "
                          "tokens is barely higher than evicted tokens. The score-based ranking provides "
                          "almost no information about token importance.")

    if hh_rand_ratio < 1.5:
        conclusions.append(f"5. **HH/Random ratio = {hh_rand_ratio:.2f}x**: HH selection performs barely "
                          "better than random selection, confirming that scores contain insufficient "
                          "signal for meaningful token triage.")

    for c in conclusions:
        lines.append(c)
        lines.append("")

    lines.append("### Overall Verdict")
    lines.append("")
    n_strong = sum([
        h_norm > 0.85,
        gini < 0.3,
        abs(pr) > 0.6,
        hh_ev_ratio < 2.0,
        hh_rand_ratio < 1.5,
    ])
    if n_strong >= 3:
        lines.append(f"**{n_strong}/5 criteria confirm HH meaninglessness.** For Llama 3.2 1B, "
                     "Heavy Hitter selection provides no actionable differentiation over sliding window. "
                     "The optimal eviction strategy is pure sliding window (keep_ratio=0.0).")
    else:
        lines.append(f"**{n_strong}/5 criteria met.** Results are mixed — further investigation "
                     "with real model output recommended.")
    lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("*Generated by `experiments/analysis/hh_proof_analysis.py`*")
    lines.append(f"*Data source: `experiments/analysis/score_distribution.csv` (simulated)*")
    lines.append("")

    return "\n".join(lines)


def main():
    if not CSV_PATH.exists():
        print(f"Error: {CSV_PATH} not found. Run the Rust test first:", file=sys.stderr)
        print(f"  cargo test -p llm_rs2 experiment_hh_proof -- --nocapture", file=sys.stderr)
        sys.exit(1)

    rows = load_csv(CSV_PATH)
    print(f"Loaded {len(rows)} tokens from {CSV_PATH}")

    report = generate_report(rows)

    REPORT_DIR.mkdir(parents=True, exist_ok=True)
    with open(REPORT_PATH, "w") as f:
        f.write(report)

    print(f"Report written to {REPORT_PATH}")
    print()
    print(report)


if __name__ == "__main__":
    main()
